package sneps.acts;





import sneps.network.ActionNode;



public abstract class ControlAction extends ActionNode {
     
	
	

	public ControlAction() {
		super();
		
		setPrimitive(true);
	}
	
	public ControlAction(String identifier) {
		super(identifier);
		
		setPrimitive(true);
	}
	
	
	
	
	
	
	
}
